package model;

public class Phone {
	private Long phoneID;
	private String name;
	private String screen;
	private String os;
	private float frontCamera;
	private float followingCamera;
	private String cpu;
	private float ram;
	private float memory;
	private String memoryCard;
	private String sim;
	private String battery;
	private int brandId;
	private long price;
	private String image;
	private int amount;
	private String brandName;
	
	public Phone() {
		super();
	}

	public Long getPhoneID() {
		return phoneID;
	}

	public void setPhoneID(Long phoneID) {
		this.phoneID = phoneID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getScreen() {
		return screen;
	}

	public void setScreen(String screen) {
		this.screen = screen;
	}

	public String getOs() {
		return os;
	}

	public void setOs(String os) {
		this.os = os;
	}

	public float getFrontCamera() {
		return frontCamera;
	}

	public void setFrontCamera(float frontCamera) {
		this.frontCamera = frontCamera;
	}

	public float getFollowingCamera() {
		return followingCamera;
	}

	public void setFollowingCamera(float followingCamera) {
		this.followingCamera = followingCamera;
	}

	public String getCpu() {
		return cpu;
	}

	public void setCpu(String cpu) {
		this.cpu = cpu;
	}

	public float getRam() {
		return ram;
	}

	public void setRam(float ram) {
		this.ram = ram;
	}

	public float getMemory() {
		return memory;
	}

	public void setMemory(float memory) {
		this.memory = memory;
	}

	public String getMemoryCard() {
		return memoryCard;
	}

	public void setMemoryCard(String memoryCard) {
		this.memoryCard = memoryCard;
	}

	public String getSim() {
		return sim;
	}

	public void setSim(String sim) {
		this.sim = sim;
	}

	public String getBattery() {
		return battery;
	}

	public void setBattery(String battery) {
		this.battery = battery;
	}

	public int getBrandId() {
		return brandId;
	}

	public void setBrandId(int brandId) {
		this.brandId = brandId;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}	
	
	
}
